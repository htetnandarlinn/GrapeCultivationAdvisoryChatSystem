<script>
const observer = new IntersectionObserver(entries => {
    entries.forEach(entry => {
        if (entry.isIntersecting) entry.target.classList.add('visible');
    });
}, { threshold: 0.1 });
document.querySelectorAll('.reveal').forEach(el => observer.observe(el));

document.getElementById('contact-form')?.addEventListener('submit', function(e) {
    e.preventDefault();
    const btn = document.getElementById('contact-submit-btn');
    const btnText = document.getElementById('contact-btn-text');
    const btnIcon = document.getElementById('contact-btn-icon');
    const btnSpinner = document.getElementById('contact-btn-spinner');
    const successEl = document.getElementById('contact-success');
    const errorEl = document.getElementById('contact-error');

    btn.disabled = true;
    btnText.textContent = 'Sending...';
    btnIcon.classList.add('hidden');
    btnSpinner.classList.remove('hidden');
    if (errorEl) errorEl.classList.add('hidden');

    const formData = new FormData(this);

    fetch('<?= BASE_URL ?>/contact', {
        method: 'POST',
        body: formData,
        headers: { 'Accept': 'application/json' }
    })
    .then(res => res.json())
    .then(data => {
        if (data.success) {
            successEl.classList.remove('hidden');
            this.reset();
        } else {
            if (errorEl) {
                errorEl.querySelector('span').textContent = data.message || 'Something went wrong.';
                errorEl.classList.remove('hidden');
            }
        }
    })
    .catch(() => {
        if (errorEl) {
            errorEl.querySelector('span').textContent = 'Network error. Please try again.';
            errorEl.classList.remove('hidden');
        }
    })
    .finally(() => {
        btn.disabled = false;
        btnText.textContent = 'Send Message';
        btnIcon.classList.remove('hidden');
        btnSpinner.classList.add('hidden');
        setTimeout(() => successEl.classList.add('hidden'), 6000);
    });
});

document.querySelectorAll('.bg-slate-50.rounded-lg button').forEach(btn => {
    btn.addEventListener('click', function() {
        const content = this.nextElementSibling;
        const icon = this.querySelector('svg');
        const isOpen = !content.classList.contains('hidden');
        document.querySelectorAll('.bg-slate-50.rounded-lg .hidden').forEach(el => {
            if (el !== content) el.classList.add('hidden');
        });
        document.querySelectorAll('.bg-slate-50.rounded-lg button svg').forEach(s => {
            if (s !== icon) s.style.transform = 'rotate(0deg)';
        });
        content.classList.toggle('hidden');
        icon.style.transform = isOpen ? 'rotate(0deg)' : 'rotate(180deg)';
    });
});
</script>